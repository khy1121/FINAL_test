function insertCorrect(correct: string, distractors: [string, string, string], index: number) {
  const answerIndex = index % 4;
  const options = [...distractors];
  options.splice(answerIndex, 0, correct);
  return { options, answerIndex };
}

function makeConceptChoice(topic: IosTopic, index: number): Question {
  const { options, answerIndex } = insertCorrect(topic.correct, topic.distractors, index);
  return {
    id: `ios-${topic.key}-concept-choice`,
    chapter: topic.chapter,
    kind: "choice",
    priority: topic.priority,
    prompt: `${topic.title}에 대한 설명으로 맞는 것은?`,
    options,
    answerIndex,
    explanation: topic.explanation,
    tags: topic.tags,
  };
}

function makeConvertedChoice(topic: IosTopic, index: number): Question {
  const { options, answerIndex } = insertCorrect(
    topic.convertedAnswer,
    [
      `${topic.title}은(는) 시험 범위와 직접 관련이 없다.`,
      `${topic.title}은(는) 항상 Firebase Storage에서만 처리한다.`,
      `${topic.title}은(는) ViewController 생명주기와 무관하게 자동 해결된다.`,
    ],
    index + 1,
  );
  return {
    id: `ios-${topic.key}-converted-choice`,
    chapter: topic.chapter,
    kind: "choice",
    priority: topic.priority,
    prompt: topic.convertedPrompt,
    options,
    answerIndex,
    explanation: topic.explanation,
    tags: [...topic.tags, "서술형 변환"],
  };
}

function makeShort(topic: IosTopic): Question {
  return {
    id: `ios-${topic.key}-short`,
    chapter: topic.chapter,
    kind: "short",
    priority: topic.priority,
    prompt: topic.shortPrompt,
    answer: topic.shortAnswer,
    keywords: topic.keywords,
    explanation: topic.explanation,
    tags: topic.tags,
  };
}

function makeDetailChoice(topic: IosTopic, index: number): Question {
  const { options, answerIndex } = insertCorrect(topic.detailCorrect, topic.detailDistractors, index + 2);
  return {
    id: `ios-${topic.key}-detail-choice`,
    chapter: topic.chapter,
    kind: "choice",
    priority: topic.priority,
    prompt: `${topic.title}의 PPT/노트 기반 시험 포인트로 맞는 것은?`,
    options,
    answerIndex,
    explanation: topic.explanation,
    tags: [...topic.tags, "PPT 기반"],
  };
}

export const iosQuestions: Question[] = topics.flatMap((topic, index) => [
  makeConceptChoice(topic, index),
  makeConvertedChoice(topic, index),
  makeShort(topic),
  makeDetailChoice(topic, index),
]);
