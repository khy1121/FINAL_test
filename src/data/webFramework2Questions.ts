function makeChoice(topic: Wf2Topic, index: number): Question {
  const answerIndex = index % 4;
  const options = [...topic.distractors];
  options.splice(answerIndex, 0, topic.correct);

  return {
    id: `wf2-${topic.key}-choice`,
    chapter: topic.chapter,
    kind: "choice",
    priority: topic.priority,
    prompt: `${topic.title}에 대한 설명으로 가장 알맞은 것은?`,
    options,
    answerIndex,
    explanation: topic.explanation,
    tags: topic.tags,
  };
}

function makeExamChoice(topic: Wf2Topic, index: number): Question {
  const answerIndex = (index + 1) % 4;
  const options = [
    `${topic.title}은(는) Spring Boot 수업 범위와 직접 관련이 없다.`,
    `${topic.title}은(는) 항상 XML 수동 설정만 허용한다.`,
    `${topic.title}은(는) 외부 Tomcat 설치를 반드시 요구한다.`,
  ];
  options.splice(answerIndex, 0, topic.essayAnswer.join(" "));

  return {
    id: `wf2-${topic.key}-exam-choice`,
    chapter: topic.chapter,
    kind: "choice",
    priority: topic.priority,
    prompt: `시험형 객관식: ${topic.essayPrompt}`,
    options,
    answerIndex,
    explanation: topic.explanation,
    tags: [...topic.tags, "서술형 변환"],
  };
}

function makeShort(topic: Wf2Topic): Question {
  return {
    id: `wf2-${topic.key}-short`,
    chapter: topic.chapter,
    kind: "short",
    priority: topic.priority,
    prompt: topic.shortPrompt,
    answer: topic.shortAnswer,
    keywords: topic.keywords,
    explanation: topic.explanation,
    tags: topic.tags,
  };
}

function makeKeywordShort(topic: Wf2Topic): Question {
  return {
    id: `wf2-${topic.key}-keyword-short`,
    chapter: topic.chapter,
    kind: "short",
    priority: topic.priority,
    prompt: `${topic.title}의 핵심 키워드를 쓰시오.`,
    answer: topic.shortAnswer,
    keywords: topic.keywords.slice(0, 1),
    explanation: topic.explanation,
    tags: [...topic.tags, "핵심 키워드"],
  };
}

export const webFramework2Questions: Question[] = topics.flatMap((topic, index) => [
  makeChoice(topic, index),
  makeExamChoice(topic, index),
  makeShort(topic),
  makeKeywordShort(topic),
]);
